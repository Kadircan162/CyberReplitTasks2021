package day33_Constructors;

public class Car {
    String make;
    String model;
    int year;
    int miles;
    String color;

    public Car(String make, String model, int year, int miles, String color){
        this.make = make;
        this.model = model;
        this.year = year;
        this.miles = miles;
        this.color = color;
    }


}
